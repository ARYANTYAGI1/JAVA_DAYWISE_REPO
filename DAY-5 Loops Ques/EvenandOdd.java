public class EvenandOdd {
    public static void main(String[] args) {
        // Even Values
        for(int i=1;i<=100;i++){
            if(i%2==0){
                System.out.println(i);
            }
        }
        // Odd Values
        for(int i=1;i<=100;i++){
            if(i%2!=0){
                System.out.println(i);
            }
        }
    }
}
